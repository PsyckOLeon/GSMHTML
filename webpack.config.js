const Encore = require('@symfony/webpack-encore');

if (!Encore.isRuntimeEnvironmentConfigured()) {
    Encore.configureRuntimeEnvironment(process.env.NODE_ENV || 'dev');
}

Encore
    .setOutputPath('public/build/')
    .setPublicPath('/build')
    .addEntry('app', './assets/app.js')
    .splitEntryChunks()
    .enableSingleRuntimeChunk()
    .cleanupOutputBeforeBuild()
    .enableBuildNotifications()
    .enableSourceMaps(!Encore.isProduction())
    .enableVersioning(Encore.isProduction())
    .configureBabelPresetEnv((config) => {
        config.useBuiltIns = 'usage';
        config.corejs = '3.23';
    })
    .enableSassLoader()
    // Importer les fonctionnalit√©s de tooltip et de popover de Bootstrap
    .addEntry('bootstrap-tooltip', './node_modules/bootstrap/js/dist/tooltip.js')
    .addEntry('bootstrap-popover', './node_modules/bootstrap/js/dist/popover.js')
;

module.exports = Encore.getWebpackConfig();
